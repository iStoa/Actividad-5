public class Act5 {
    public static void main(String[] args) {
        double X = 15;
        double Y = 26;
        double suma, resta, multi, div, modulo;


        suma = X+Y;
        resta = X-Y;
        multi = X*Y;
        div = X / Y;
        modulo = X % Y;


        System.out.println("La suma de " +X+ " y "+Y+" es:"+suma);
        System.out.println("La resta de " +X+ " y "+Y+" es: "+resta);
        System.out.println("La multiplicacion de " +X+ " y " +Y+" es: "+multi);
        System.out.println("La division de "+Y+" y "+X+" es: "+div);
        System.out.println("El modulo de "+Y+" y "+X+" es: "+modulo);
    }
}
